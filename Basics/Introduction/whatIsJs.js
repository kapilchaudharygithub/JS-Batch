/** Defination
 * JS is a very versatile  Synchronous, Single-Threaded, Multi-Purpose , Multi-Paradigm , Dynamic Loosely
 *  (Weakely) Typed , Scripting as well as Programming Language
 * 
 * Synchronous-->Execution is being done line by line
 * 
 * Single-Threaded--> One Task at a time becuse JS has only 1 call stack
 *                     Means only execution of one line at a time in a specific order.
 * 
 * Multi-purpose ---> We can use it for multiple purposes like Creating:
 *                    Mobile App,
 *                    Desktop App,
 *                    Backend Server,
 *                    Chrome Extensions,
 *                    Scripts, 
 *                    and much more.
 * 
 * Multi-Paradigm: Means Way of writing/ style of code:
 *                  Functional
 *                  Procedural
 *                  Object Oriented
 * 
 * 
 * Dynamic Loosely/Weakely Typed Language:  Allows you to declare a variable  without specifying it's type,
 *                                           also we can store any type of value in a single variable.
 * 
 * 
 * 
 */